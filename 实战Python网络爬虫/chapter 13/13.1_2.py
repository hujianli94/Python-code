str = 'ABCABCABC'
# 单个内容替换
print(str.replace('C', 'V'))
# 输出内容：ABVABVABV

# 字符串替换
print(str.replace('BC', 'WV'))
# 输出内容：AWVAWVAWV
# 替换成特殊符号(空格)
print(str.replace('BC', ' '))
# 输出内容：A A A
