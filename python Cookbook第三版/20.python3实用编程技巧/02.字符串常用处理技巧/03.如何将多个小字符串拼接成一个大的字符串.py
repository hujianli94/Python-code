l = ["<11>", "<22>", "<33>", "<44>", "<55>", "<66>", "<77>"]

# 第一种方式
s = ''
for x in l:
    s += x
print(s)  # <11><22><33><44><55><66><77>

# 第二种方式
s2 = ''.join(l)
print(s2)  # <11><22><33><44><55><66><77>
